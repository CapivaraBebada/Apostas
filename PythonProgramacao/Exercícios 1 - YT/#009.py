#Monte um algoritmo que çeia um número, mostre seu dobro, triplo e raíz quadrada

a=float(input('Digite um número: '))
do=(a*2)
triplo=(a*3)
raiz=(a**(1/2))
print('O dobro do número é: {}'.format(do))
print('O triplo do número é: {}'.format(triplo))
print('A sua raíz é {:.3f}'.format(raiz))
