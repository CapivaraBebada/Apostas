'''
    Estrutura de Repetição

        Usado para usar difversos dados, mas que não precise de muitas
    linhas para o código ser executado.

    Repetição FOR

        Usada quando sabemos a quantidade de interações que queremos.

'''
print('Exemplo usando "for":')
numero = int(input('O programa contará de 0 até o número que vc digitar: '))
for i in range(0, numero + 1):
    print(i)